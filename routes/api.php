Route::middleware('auth:sanctum')->get('/user', function (Request $request) {
    return $request->user();
});

Route::apiResource('products', ProductController::class);
Route::post('/api/upload/local', [ProductController::class,
'uploadImageLocal']);
Route::post('/api/upload/public', [ProductController::class,
'uploadImagePublic']);

Route::middleware('token')->group(function () {
    Route::post('/products', [ProductController::class, 'store']);
    Route::put('/products/{id}', [ProductController::class, 'update']);
    Route::delete('/products/{id}', [ProductController::class, 'destroy']);
    Route::post('/upload/local', [ProductController::class, 'uploadImageLocal']);
    Route::post('/upload/public', [ProductController::class, 'uploadImagePublic']);
    
});

